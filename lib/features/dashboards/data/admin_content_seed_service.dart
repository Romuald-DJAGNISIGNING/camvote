import '../../../core/network/worker_client.dart';

class SeedReport {
  final int civicLessons;
  final int electionCalendar;
  final int transparencyUpdates;
  final int observationChecklist;
  final int votingCenters;
  final int legalDocuments;
  final bool electionsInfo;

  const SeedReport({
    required this.civicLessons,
    required this.electionCalendar,
    required this.transparencyUpdates,
    required this.observationChecklist,
    required this.votingCenters,
    required this.legalDocuments,
    required this.electionsInfo,
  });
}

class AdminContentRecord {
  final String id;
  final Map<String, dynamic> data;

  const AdminContentRecord({required this.id, required this.data});
}

class AdminContentSeedService {
  AdminContentSeedService({WorkerClient? workerClient})
    : _workerClient = workerClient ?? WorkerClient();

  final WorkerClient _workerClient;

  Future<SeedReport> seedCameroonContent({
    bool overwrite = false,
    bool includeCenters = false,
  }) async {
    final civicLessons = _civicLessons();
    final calendar = _electionCalendar();
    final transparency = _transparencyUpdates();
    final checklist = _observationChecklist();
    final legalDocs = _legalDocuments();
    final electionsInfo = _electionsInfo();
    final centers = includeCenters
        ? _votingCenters()
        : const <Map<String, dynamic>>[];

    final response = await _workerClient.post(
      '/v1/admin/content/seed',
      data: {
        'overwrite': overwrite,
        'purgeBeforeSeed': overwrite,
        'includeCenters': includeCenters,
        'civicLessons': civicLessons,
        'electionCalendar': calendar,
        'transparencyUpdates': transparency,
        'observationChecklist': checklist,
        'legalDocuments': legalDocs,
        'votingCenters': centers,
        'electionsInfo': electionsInfo,
      },
    );

    return SeedReport(
      civicLessons: _asInt(response['civicLessons']),
      electionCalendar: _asInt(response['electionCalendar']),
      transparencyUpdates: _asInt(response['transparencyUpdates']),
      observationChecklist: _asInt(response['observationChecklist']),
      votingCenters: _asInt(response['votingCenters']),
      legalDocuments: _asInt(response['legalDocuments']),
      electionsInfo: _asBool(response['electionsInfo']),
    );
  }

  Future<List<AdminContentRecord>> fetchItems({
    required String collection,
    String? locale,
  }) async {
    final response = await _workerClient.get(
      '/v1/admin/content',
      queryParameters: {
        'collection': collection,
        if (locale != null && locale.trim().isNotEmpty) 'locale': locale.trim(),
      },
    );
    final items = response['items'];
    if (items is! List) return const [];
    return items.whereType<Map>().map((entry) {
      final map = entry.cast<String, dynamic>();
      final data =
          (map['data'] as Map?)?.cast<String, dynamic>() ??
          const <String, dynamic>{};
      return AdminContentRecord(
        id: (map['id']?.toString() ?? '').trim(),
        data: data,
      );
    }).toList();
  }

  Future<AdminContentRecord> upsertItem({
    required String collection,
    required String id,
    required Map<String, dynamic> data,
  }) async {
    final response = await _workerClient.post(
      '/v1/admin/content/upsert',
      data: {'collection': collection, 'id': id, 'data': data},
    );
    final item =
        (response['item'] as Map?)?.cast<String, dynamic>() ??
        const <String, dynamic>{};
    return AdminContentRecord(id: id, data: item);
  }

  Future<void> deleteItem({
    required String collection,
    required String id,
  }) async {
    await _workerClient.post(
      '/v1/admin/content/delete',
      data: {'collection': collection, 'id': id},
    );
  }

