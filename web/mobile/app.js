function setStoreLinks() {
  const params = new URLSearchParams(window.location.search);
  const play = params.get('play') || DEFAULT_PLAY;
  const iosLive = params.get('ios_live') === '1';
  const requestedApp = params.get('app') || DEFAULT_APP;
  const app = iosLive ? requestedApp : DEFAULT_APP;
  const publicUrl = params.get('public') || DEFAULT_PUBLIC;
  const auto = params.get('auto');
  const deeplinkAndroid = params.get('deeplink_android') || params.get('deeplink');
  const deeplinkIos = params.get('deeplink_ios') || params.get('deeplink');
  const playCta = document.getElementById('playCta');
  const appCta = document.getElementById('appCta');
  const playCtaBottom = document.getElementById('playCtaBottom');
  const appCtaBottom = document.getElementById('appCtaBottom');
  const downloadCtaRow = document.getElementById('downloadCtaRow');
  const ctaBodyText = document.getElementById('ctaBodyText');
  const smartQr = document.getElementById('smartQr');
  const qrWrap = document.getElementById('qrWrap');
  const qrCard = document.getElementById('qrSmartCard');
  const qrBurst = document.getElementById('qrBurst');
  const dict = copy[currentLang] || copy.en;
  const ua = (navigator.userAgent || '').toLowerCase();
  const isAndroid = ua.includes('android');
  const isIos = ua.includes('iphone') ||
    ua.includes('ipad') ||
    ua.includes('ipod') ||
    (ua.includes('macintosh') && ua.includes('mobile'));
  const isMobile = isAndroid || isIos;
  const isDesktop = !isMobile;

  const hasPlay = !!play;
  const hasApp = !!app;

  const setLink = (el, url) => {
    if (!el) return;
    if (url) {
      el.href = url;
      el.style.display = 'inline-flex';
    } else {
      el.style.display = 'none';
    }
  };

  const showPlay = hasPlay && (!isMobile || isAndroid);
  const showApp = hasApp && (!isMobile || isIos);

  const appWithParams = appendParams(app, {
    lang: currentLang || 'en',
    public: publicUrl
  });
  const appFinal = ensureAppStoreIndex(appWithParams);
  const resolvedPlay = resolveUrl(play);
  const resolvedApp = resolveUrl(appFinal);

  setLink(playCta, showPlay ? resolvedPlay : '');
  setLink(playCtaBottom, showPlay ? resolvedPlay : '');
  setLink(appCta, showApp ? resolvedApp : '');
  setLink(appCtaBottom, showApp ? resolvedApp : '');
  if (downloadCtaRow) {
    downloadCtaRow.style.display = (showPlay || showApp) ? 'flex' : 'none';
  }

  if (ctaBodyText) {
    const ctaKey = isAndroid ? 'ctaBodyAndroid' : (isIos ? 'ctaBodyIos' : 'ctaBody');
    ctaBodyText.textContent =
      dict[ctaKey] ||
      copy.en[ctaKey] ||
      dict.ctaBody ||
      copy.en.ctaBody;
  }

  if (auto && isMobile) {
    if (isAndroid && resolvedPlay) {
      startAutoRedirect(resolvedPlay, 'android', deeplinkAndroid);
    } else if (isIos && resolvedApp) {
      startAutoRedirect(resolvedApp, 'ios', deeplinkIos);
    }
  }

  if (qrWrap) {
    if (isDesktop && (hasPlay || hasApp)) {
      const smartUrl = buildSmartUrl(
        play,
        app,
        currentLang || 'en',
        publicUrl,
        deeplinkAndroid,
        deeplinkIos,
        iosLive
      );
      if (smartQr) {
        setQrImageWithFallback(smartQr, smartUrl, 220);
      }
      qrWrap.classList.add('active');
      enableQrCardInteraction(qrCard, qrBurst);
    } else {
      qrWrap.classList.remove('active');
      if (qrCard) {
        qrCard.classList.remove('scanning');
      }
    }
  }
}

function setQrImageWithFallback(imageEl, payload, size) {
  if (!imageEl) return;
  const sources = buildQrSources(payload, size);
  if (sources.length === 0) {
    imageEl.removeAttribute('src');
    return;
  }

  let index = 0;
  const loadNext = () => {
    if (index >= sources.length) {
      imageEl.removeAttribute('src');
      imageEl.classList.add('qr-error');
      return;
    }
    const next = sources[index];
    index += 1;
    imageEl.classList.remove('qr-error');
    imageEl.src = next;
  };

  imageEl.onerror = () => {
    loadNext();
  };
  imageEl.onload = () => {
    imageEl.classList.remove('qr-error');
  };
  loadNext();
}

function buildQrSources(payload, size) {
  const value = `${payload || ''}`.trim();
  if (!value) return [];
  const encoded = encodeURIComponent(value);
  const qrSize = Number.isFinite(size) ? Math.max(160, Math.min(size, 320)) : 220;
  return [
    `https://api.qrserver.com/v1/create-qr-code/?size=${qrSize}x${qrSize}&data=${encoded}&margin=2&ecc=H`,
    `https://quickchart.io/qr?size=${qrSize}&text=${encoded}&ecLevel=H&margin=2`,
    `https://chart.googleapis.com/chart?chs=${qrSize}x${qrSize}&cht=qr&chl=${encoded}&choe=UTF-8`,
  ];
}

function resolveUrl(url) {
  if (!url) return '';
  if (/^[a-z][a-z0-9+.-]*:/i.test(url)) {
    return url;
  }
  const protocol = window.location.protocol;
  if (protocol === 'file:') {
    let resolved = url;
    if (resolved.startsWith('/mobile/')) {
      resolved = resolved.replace('/mobile/', './');
    } else if (resolved.startsWith('/')) {
      resolved = `.${resolved}`;
    }
    const [pathPart, hashPart] = resolved.split('#');
    const [pathOnly, queryPart] = pathPart.split('?');
    if (pathOnly.endsWith('/app-store') || pathOnly.endsWith('/app-store/')) {
      const basePath = pathOnly.endsWith('/') ? pathOnly : `${pathOnly}/`;
      const withIndex = `${basePath}index.html`;
      const withQuery = queryPart ? `${withIndex}?${queryPart}` : withIndex;
      return hashPart ? `${withQuery}#${hashPart}` : withQuery;
    }
    return resolved;
  }
  const base = window.location.origin.replace(/\/$/, '');
  if (url.startsWith('/')) return `${base}${url}`;
  return `${base}/${url}`;
}

function appendParams(url, params) {
  if (!url) return url;
  const [pathAndQuery, hash] = url.split('#');
  const [base, query] = pathAndQuery.split('?');
  const searchParams = new URLSearchParams(query || '');
  Object.entries(params).forEach(([key, value]) => {
    if (value) {
      searchParams.set(key, value);
    }
  });
  const queryString = searchParams.toString();
  const next = `${base}${queryString ? `?${queryString}` : ''}`;
  return hash ? `${next}#${hash}` : next;
}

function ensureAppStoreIndex(url) {
  if (!url) return url;
  const [pathAndQuery, hash] = url.split('#');
  const [base, query] = pathAndQuery.split('?');
  if (base.endsWith('/app-store') || base.endsWith('/app-store/')) {
    const normalized = base.endsWith('/') ? base : `${base}/`;
    const withIndex = `${normalized}index.html`;
    const withQuery = query ? `${withIndex}?${query}` : withIndex;
    return hash ? `${withQuery}#${hash}` : withQuery;
  }
  return url;
}

function ensureBurstParticles(container) {
  if (!container) return;
  if (container.children.length) return;
  for (let i = 0; i < BURST_PARTICLE_COUNT; i += 1) {
    const dot = document.createElement('span');
    const angle = (360 / BURST_PARTICLE_COUNT) * i;
    dot.style.setProperty('--burst-angle', `${angle}deg`);
    container.appendChild(dot);
  }
}

function triggerBurst(container) {
  if (!container) return;
  ensureBurstParticles(container);
  container.classList.remove('active');
  void container.offsetWidth;
  container.classList.add('active');
}

function enableQrCardInteraction(card, burst) {
  if (!card || card.dataset.enhanced === '1') return;
  ensureBurstParticles(burst);
  card.dataset.enhanced = '1';
  card.classList.add('scanning');
  triggerBurst(burst);
  window.setTimeout(() => {
    card.classList.remove('scanning');
  }, 1200);

  card.addEventListener('mouseenter', () => {
    card.classList.add('scanning');
  });
  card.addEventListener('mouseleave', () => {
    card.classList.remove('scanning');
  });
  card.addEventListener('click', () => {
    card.classList.add('scanning');
    triggerBurst(burst);
    window.setTimeout(() => {
      card.classList.remove('scanning');
    }, 900);
  });
}

function startAutoRedirect(url, kind, deepLink) {
  if (!url) return;
  if (autoRedirectStarted) return;
  autoRedirectStarted = true;
  const prefersReduced = window.matchMedia('(prefers-reduced-motion: reduce)').matches;
  if (prefersReduced) {
    if (deepLink) {
      attemptDeepLink(deepLink, url);
    } else {
      window.location.href = url;
    }
    return;
  }
  const overlay = document.getElementById('scanOverlay');
  const scanCard = overlay ? overlay.querySelector('.scan-card') : null;
  const androidLogo = document.getElementById('scanLogoAndroid');
  const iosLogo = document.getElementById('scanLogoIos');
  const scanBurst = document.getElementById('scanBurst');
  if (androidLogo) {
    androidLogo.style.display = kind === 'android' ? 'flex' : 'none';
  }
  if (iosLogo) {
    iosLogo.style.display = kind === 'ios' ? 'flex' : 'none';
  }
  ensureBurstParticles(scanBurst);
  if (scanCard) {
    scanCard.classList.remove('scanning', 'complete');
  }
  if (scanBurst) {
    scanBurst.classList.remove('active');
  }
  if (overlay) {
    overlay.classList.add('active');
  }
  window.requestAnimationFrame(() => {
    if (scanCard) {
      scanCard.classList.add('scanning');
    }
  });
  window.setTimeout(() => {
    if (scanCard) {
      scanCard.classList.add('complete');
    }
    triggerBurst(scanBurst);
  }, 980);

  const redirect = () => {
    if (deepLink) {
      attemptDeepLink(deepLink, url);
      return;
    }
    window.location.href = url;
  };

  window.setTimeout(() => {
    redirect();
  }, AUTO_REDIRECT_DELAY_MS);
}

function attemptDeepLink(deepLink, fallbackUrl) {
  let completed = false;
  let timerId = null;
  const cleanup = () => {
    if (completed) return;
    completed = true;
    document.removeEventListener('visibilitychange', onVisibility);
    if (timerId) {
      window.clearTimeout(timerId);
      timerId = null;
    }
  };
  const onVisibility = () => {
    if (document.hidden) {
      cleanup();
    }
  };
  document.addEventListener('visibilitychange', onVisibility);
  timerId = window.setTimeout(() => {
    if (!completed) {
      cleanup();
      window.location.href = fallbackUrl;
    }
  }, 1400);
  window.location.href = deepLink;
}

function buildSmartUrl(play, app, lang, publicUrl, deepAndroid, deepIos, iosLive) {
  const base = new URL(window.location.href);
  base.search = '';
  base.hash = '';
  if (play) base.searchParams.set('play', play);
  if (app) base.searchParams.set('app', app);
  if (lang) base.searchParams.set('lang', lang);
  if (publicUrl) base.searchParams.set('public', publicUrl);
  if (deepAndroid) base.searchParams.set('deeplink_android', deepAndroid);
  if (deepIos) base.searchParams.set('deeplink_ios', deepIos);
  if (iosLive) base.searchParams.set('ios_live', '1');
  base.searchParams.set('auto', '1');
  return base.toString();
}

